const I18nContext = createContext<I18nContextProps | undefined>(undefined);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>('en');

  useEffect(() => {
    const saved = localStorage.getItem('libreetl_lang') as Language;
    if (saved && translations[saved]) {
      setLanguageState(saved);
    } else {
      const browserLang = navigator.language.split('-')[0];
      if (['en', 'pt', 'es'].includes(browserLang)) {
        setLanguageState(browserLang as Language);
      }
    }
  }, []);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('libreetl_lang', lang);
  };

  const t = (key: string, vars?: Record<string, string | number>): string => {
    let str = translations[language]?.[key] ?? translations['en']?.[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, String(v));
      });
    }
    return str;
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used within I18nProvider');
  return context;
};

export const tGlobal = (key: string, vars?: Record<string, string | number>): string => {
  if (typeof window === 'undefined') return key;
  let lang = localStorage.getItem('libreetl_lang') as Language;
  if (!lang || !translations[lang]) {
    const browserLang = navigator.language.split('-')[0];
    lang = ['en', 'pt', 'es'].includes(browserLang) ? (browserLang as Language) : 'en';
  }
  let str = translations[lang]?.[key] ?? translations['en']?.[key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(`{${k}}`, String(v));
    });
  }
  return str;
};
